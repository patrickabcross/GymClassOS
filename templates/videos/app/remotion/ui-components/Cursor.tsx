import React from "react";

/**
 * Cursor Component - Animated cursor for Remotion compositions
 *
 * Displays a cursor (arrow, pointer hand, or text I-beam) at specified coordinates.
 * Useful for screen recording simulations, UI demonstrations, and interactive tutorials.
 */

export type CursorType = "default" | "pointer" | "text";

export type CursorProps = {
  /** X position in pixels (from left edge) */
  x?: number;
  /** Y position in pixels (from top edge) */
  y?: number;
  /** Cursor type */
  type?: CursorType;
  /** Auto cursor type from hover zones (overrides type when provided) */
  autoType?: CursorType;
  /** Overall opacity */
  opacity?: number;
  /** Scale factor */
  scale?: number;
  /** Is currently clicking (triggers click animation) */
  isClicking?: boolean;
  /** Current frame (for click animation timing) */
  currentFrame?: number;
  /** Array of frames where clicks start (for multi-click animations) */
  clickStartFrames?: number[];
};

export const Cursor: React.FC<CursorProps> = ({
  x = 0,
  y = 0,
  type = "default",
  autoType,
  opacity = 1,
  scale = 1,
  isClicking = false,
  currentFrame = 0,
  clickStartFrames = [],
}) => {
  // Click animation: quick scale down and back up over 6 frames
  // Check all click start frames to find if any animation should be playing
  let clickScale = 1;
  const clickAnimationDuration = 6;

  for (const clickFrame of clickStartFrames) {
    const framesSinceClick = currentFrame - clickFrame;
    if (framesSinceClick >= 0 && framesSinceClick < clickAnimationDuration) {
      // 0→3: scale down to 0.85
      // 3→6: scale back up to 1.0
      if (framesSinceClick < 3) {
        clickScale = 1 - (framesSinceClick / 3) * 0.15;
      } else {
        clickScale = 0.85 + ((framesSinceClick - 3) / 3) * 0.15;
      }
      break; // Only play one click animation at a time
    }
  }

  const finalScale = scale * clickScale;

  // Use autoType if provided (from hover zones), otherwise use manual type
  const effectiveType = autoType ?? type;

  return (
    <div
      style={{
        position: "absolute",
        left: x,
        top: y,
        opacity,
        transform: `scale(${finalScale})`,
        transformOrigin: "top left",
        pointerEvents: "none",
        zIndex: 9999,
      }}
    >
      {effectiveType === "default" && <DefaultCursor />}
      {effectiveType === "pointer" && <PointerCursor />}
      {effectiveType === "text" && <TextCursor />}
    </div>
  );
};

